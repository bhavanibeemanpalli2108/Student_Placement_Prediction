import json
import os
import re

from dotenv import load_dotenv
from groq import Groq


load_dotenv()

client = Groq(
    api_key=os.getenv(
        "GROQ_API_KEY"
    )
)

MODEL = "llama-3.3-70b-versatile"


SYSTEM_PROMPT = """
You are an AI Placement Mentor and Career Analytics Assistant integrated into a Student Placement Prediction and Skill Gap Analysis System.

You will receive:

1. Student academic profile
2. Technical skill scores
3. Soft skill scores
4. Placement probability generated by a Machine Learning model
5. Placement prediction status
6. Identified skill gaps

Your objective is to help students understand their employability and improve their placement readiness.

ANALYSIS OBJECTIVES

- Analyze the student's profile holistically.
- Identify the strongest factors helping placement chances.
- Identify the weakest factors reducing placement chances.
- Explain skill gaps clearly and professionally.
- Generate practical improvement recommendations.
- Prioritize recommendations based on impact.

PLACEMENT LEVELS

0-40:
High Risk

41-60:
Needs Improvement

61-80:
Placement Ready

81-100:
Highly Employable

RECOMMENDATION RULES

- Recommendations must be specific.
- Recommendations must be measurable.
- Recommendations must directly address weaknesses.
- Do not provide generic advice.
- Keep recommendations concise.
- Each recommendation should be actionable.
- Focus on employability improvement.

GOOD EXAMPLES

✓ Complete 2 internships related to your branch.
✓ Solve 150 DSA problems in the next 2 months.
✓ Build 2 portfolio projects showcasing technical skills.
✓ Participate in 3 mock interviews.
✓ Improve communication through weekly presentations.

BAD EXAMPLES

✗ Improve coding.
✗ Work harder.
✗ Gain experience.
✗ Practice more.

Return ONLY valid JSON.
"""


def build_prompt(
    student_data,
    prediction_result,
    skill_gaps
):

    return f"""
STUDENT PROFILE

CGPA: {student_data['cgpa']}
Branch: {student_data['branch']}
Backlogs: {student_data['backlogs']}

Internships Count: {student_data['internships_count']}
Projects Count: {student_data['projects_count']}

Coding Skill Score: {student_data['coding_skill_score']}
Mock Interview Score: {student_data['mock_interview_score']}
Logical Reasoning Score: {student_data['logical_reasoning_score']}
Communication Skill Score: {student_data['communication_skill_score']}
Aptitude Score: {student_data['aptitude_score']}
Leadership Score: {student_data['leadership_score']}
Extracurricular Score: {student_data['extracurricular_score']}

MODEL OUTPUT

Placement Status:
{prediction_result['prediction']}

Placement Probability:
{prediction_result['placement_probability']}%

IDENTIFIED SKILL GAPS

{chr(10).join(skill_gaps)}

Return ONLY JSON in this format:

{{
    "placement_probability": "{prediction_result['placement_probability']}%",
    "employability_level": "",

    "strengths": [
        "",
        "",
        ""
    ],

    "skill_gaps": [
        "",
        "",
        ""
    ],

    "actionable_insights": [
        "",
        "",
        "",
        "",
        ""
    ],

    "final_recommendation": ""
}}

Requirements:

- Exactly 3 strengths.
- Exactly 3 skill gaps.
- Exactly 5 actionable insights.
- Recommendations should be placement-focused.
- Keep insights concise.
- Return valid JSON only.
"""


def clean_json_response(
    response_text
):

    response_text = re.sub(
        r"```json",
        "",
        response_text
    )

    response_text = re.sub(
        r"```",
        "",
        response_text
    )

    return response_text.strip()


def generate_recommendation(
    student_data,
    prediction_result,
    skill_gaps
):

    prompt = build_prompt(
        student_data,
        prediction_result,
        skill_gaps
    )

    try:

        response = (
            client.chat.completions.create(
                model=MODEL,
                temperature=0.3,
                messages=[
                    {
                        "role": "system",
                        "content": SYSTEM_PROMPT
                    },
                    {
                        "role": "user",
                        "content": prompt
                    }
                ]
            )
        )

        result = (
            response
            .choices[0]
            .message
            .content
        )

        result = clean_json_response(
            result
        )

        return json.loads(
            result
        )

    except Exception as e:

        return {

            "placement_probability":
                f"{prediction_result['placement_probability']}%",

            "employability_level":
                "Unknown",

            "strengths": [],

            "skill_gaps":
                skill_gaps,

            "actionable_insights": [],

            "final_recommendation":
                f"Recommendation generation failed: {str(e)}"
        }